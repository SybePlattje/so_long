#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	void	*output;

	if (!dest && !src)
		return (0);
	output = dest;
	if (src < dest)
	{
		src += n;
		dest += n;
		while (n--)
			*(char *)--dest = *(char *)--src;
	}
	else
		while (n--)
			*(char *)dest++ = *(char *)src++;
	return (output);
}
